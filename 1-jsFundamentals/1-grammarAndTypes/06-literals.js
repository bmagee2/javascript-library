// Literals -- data values that we as developers use to initialize the code

//strong literal
let car = "ford";

//numeric literal
let december = 12;

//boolean literal
let tired = false;

//object literal
let soup = {
    a: "chicken noodle",     
    b: "tomato",
    c: "veg"
}

console.log(soup.a);         // dot notation

//array literal
let days = ["monday", "tuesday", "wednesday", "thursday", "friday"];

console.log(days.toString());
console.log(days.length);



