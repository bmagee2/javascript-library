//comparison operators -- 

//equal operator-- doesn't check for data type equality
console.log("3" == 3);

//strict equal operator does check for data type equality
console.log("3" === 3);

//not equal
console.log("3" != 3);

//strict not equal
console.log("3" !== 3);

//greater than
console.log(3 > 1);

//less than
console.log(3 < 1);

//greater than or equal to
console.log(5 >= 3);

//less than or equal to
console.log(3 <= 1);

//and 
console.log(1 > 0 && 2 < 1);        //checks both

//or
console.log(1 < 0 || 2 < 7);        //looks for either conditions to be true
